package com.lyht.business.datasearch.formbean;

import java.io.Serializable;

public class ComUploadFormBean implements Serializable {
	
	private static final long serialVersionUID = 1L;
	
	private String[] tabTitle;
	private String[] sqlTitle;
	private String sql;
	private String fileName;
	private String tabType;//表格类型，1是需要分省市，2不需要直接按行导出
	
	
	public String getTabType() {
		return tabType;
	}
	public void setTabType(String tabType) {
		this.tabType = tabType;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String[] getTabTitle() {
		return tabTitle;
	}
	public void setTabTitle(String[] tabTitle) {
		this.tabTitle = tabTitle;
	}
	public String[] getSqlTitle() {
		return sqlTitle;
	}
	public void setSqlTitle(String[] sqlTitle) {
		this.sqlTitle = sqlTitle;
	}
	public String getSql() {
		return sql;
	}
	public void setSql(String sql) {
		this.sql = sql;
	}
	
}
