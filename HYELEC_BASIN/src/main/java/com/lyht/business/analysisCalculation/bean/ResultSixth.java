package com.lyht.business.analysisCalculation.bean;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

import com.sun.org.glassfish.gmbal.Description;

/**
 * 说明:产流计算第六步表格实体类
 * @author 刘魁
 *@时间 2018/7/11 20:40
 */
@Entity
@Table(name = "C_RESULT_SIXTH")
public class ResultSixth implements Serializable{
		
	private static final long serialVersionUID = 1L;

	@Description(key = "primary", value = "P")
	private double p;
	
	@Description(key = "primary", value = "EM")
	private double e;
	
	@Description(key = "primary", value = "PE")
	private double pe;
	
	@Description(key = "primary", value = "EPE")
	private double epe;
	
	@Description(key = "primary", value = "PAPE")
	private double pape;
	
	@Description(key = "primary", value = "ERC")
	private double erc;
	
	@Description(key = "primary", value = "SDRC")
	private double sdrc;
	
	@Description(key = "primary", value = "R")
	private double r;
	
	@Description(key = "primary", value = "RGDX")
	private double rgdx;
	
	@Description(key = "primary", value = "ER")
	private double er;
	
	@Description(key="primary",value="时间")
	private String tm;
	
	@Description(key="primary",value="主键")
	private String id;
	
	@Description(key="primary",value="批次号")
	private String pch;
	
	@Description(key="primary",value="测站编码 ")
	private String stcd;
	
	@Column(name = "P",length=50 )
	public double getP() {
		return p;
	}

	public void setP(double p) {
		this.p = p;
	}
	@Column(name = "E",length=50 )
	public double getE() {
		return e;
	}

	public void setE(double e) {
		this.e = e;
	}
	@Column(name = "PE",length=50 )
	public double getPe() {
		return pe;
	}

	public void setPe(double pe) {
		this.pe = pe;
	}
	@Column(name = "EPE",length=50 )
	public double getEpe() {
		return epe;
	}

	public void setEpe(double epe) {
		this.epe = epe;
	}
	@Column(name = "PAPE",length=50 )
	public double getPape() {
		return pape;
	}

	public void setPape(double pape) {
		this.pape = pape;
	}
	@Column(name = "ERC",length=50 )
	public double getErc() {
		return erc;
	}

	public void setErc(double erc) {
		this.erc = erc;
	}
	@Column(name = "SDRC",length=50 )
	public double getSdrc() {
		return sdrc;
	}

	public void setSdrc(double sdrc) {
		this.sdrc = sdrc;
	}
	@Column(name = "R",length=50 )
	public double getR() {
		return r;
	}

	public void setR(double r) {
		this.r = r;
	}
	@Column(name = "RGDX",length=50 )
	public double getRgdx() {
		return rgdx;
	}

	public void setRgdx(double rgdx) {
		this.rgdx = rgdx;
	}
	@Column(name = "ER",length=50 )
	public double getEr() {
		return er;
	}

	public void setEr(double er) {
		this.er = er;
	}
	@Column(name = "TM",length=50 )
	public String getTm() {
		return tm;
	}

	public void setTm(String tm) {
		this.tm = tm;
	}
	@Id
	@Column(name = "ID",length=50 )
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	@Column(name = "PCH",length=50 )
	public String getPch() {
		return pch;
	}

	public void setPch(String pch) {
		this.pch = pch;
	}
	@Column(name = "STCD",length=50 )
	public String getStcd() {
		return stcd;
	}

	public void setStcd(String stcd) {
		this.stcd = stcd;
	}

	@Override
	public String toString() {
		return "ResultSixth [p=" + p + ", e=" + e + ", pe=" + pe + ", epe=" + epe + ", pape=" + pape + ", erc=" + erc
				+ ", sdrc=" + sdrc + ", r=" + r + ", rgdx=" + rgdx + ", er=" + er + ", tm=" + tm + ", id=" + id
				+ ", pch=" + pch + ", stcd=" + stcd + "]";
	}


	
	public ResultSixth() {
		this.e=0;
		this.epe=0;
		this.er=0;
		this.erc=0;
		this.id="";
		this.p=0;
		this.pape=0;
		this.pch="";
		this.pe=0;
		this.r=0;
		this.rgdx=0;
		this.sdrc=0;
		this.stcd="";
		this.tm="";
	
	}
	
	
}
