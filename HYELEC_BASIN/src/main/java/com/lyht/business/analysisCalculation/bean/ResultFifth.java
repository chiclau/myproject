package com.lyht.business.analysisCalculation.bean;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

import com.sun.org.glassfish.gmbal.Description;
/**
 * 第五步保存表
 * @author Administrator
 *
 */
@Entity
@Table(name = "C_RESULT_FIFTH")
public class ResultFifth  implements Serializable {


	private static final long serialVersionUID = 1L;
	
	@Description(key="primary",value="主键")
	private String id;

	@Description(key="primary",value="时间")
	private String date;
	
	@Description(key="primary",value="测站编码")
	private String stcd;
	
	@Description(key="primary",value="雨面量")
	private double yml;
	
	@Description(key="primary",value="EM")
	private double em;
	
	@Description(key="primary",value="Pa")
	private double pa;
	
	@Description(key="primary",value="批次号")
	private String pch;
	
	@Description(key="primary",value="STNM")
	private String stnm;
	
	@Description(key="primary",value="QZ")
	private String qz;
	
	@Description(key="primary",value="JYL")
	private String jyl;

	@Description(key="primary",value="INTERVAL")
	private String interval;
	
	@Id
	@Column(name = "ID",length=50 )
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	@Column(name = "DATE",length=50 )
	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}
	@Column(name = "STCD",length=50 )
	public String getStcd() {
		return stcd;
	}

	public void setStcd(String stcd) {
		this.stcd = stcd;
	}
	@Column(name = "YML",length=50 )
	public double getYml() {
		return yml;
	}

	public void setYml(double yml) {
		this.yml = yml;
	}
	@Column(name = "EM",length=50 )
	public double getEm() {
		return em;
	}

	public void setEm(double em) {
		this.em = em;
	}
	@Column(name = "PA",length=50 )
	public double getPa() {
		return pa;
	}

	public void setPa(double pa) {
		this.pa = pa;
	}
	@Column(name = "PCH",length=50 )
	public String getPch() {
		return pch;
	}

	public void setPch(String pch) {
		this.pch = pch;
	}
	@Column(name = "STNM",length=50 )
	public String getStnm() {
		return stnm;
	}

	public void setStnm(String stnm) {
		this.stnm = stnm;
	}
	@Column(name = "QZ",length=50 )
	public String getQz() {
		return qz;
	}

	public void setQz(String qz) {
		this.qz = qz;
	}
	@Column(name = "JYL",length=50 )
	public String getJyl() {
		return jyl;
	}

	public void setJyl(String jyl) {
		this.jyl = jyl;
	}
	@Column(name = "INTERVAL",length=50 )
	public String getInterval() {
		return interval;
	}

	public void setInterval(String interval) {
		this.interval = interval;
	}
	
	
	
	
}
